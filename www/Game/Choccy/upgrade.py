# upgrade class
# Osi's The Wastes

class upgrade:
    def __init__(self):
        self.name = ""

        self.AP = 0
        self.acceleration = 0
        self.handling = 0
        self.braking = 0
        self.capability = 0

        self.value = 0

        self.description = ""

    def erase(self):
        self.name = ""

        self.AP = 0
        self.acceleration = 0
        self.handling = 0
        self.braking = 0
        self.capability = 0

        self.value = 0

        self.description = ""